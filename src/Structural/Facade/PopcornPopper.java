public class PopcornPopper {
    public void on() {
        System.out.println("팝콘 기계가 켜졌습니다.");
    }

    public void pop() {
        System.out.println("팝콘이 나옵니다.");
    }

    public void off() {
        System.out.println("팝콘 기계 전원을 끕니다.");
    }
}
